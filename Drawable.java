/**
 * The Drawable interface defines a standard method for game elements 
 * in the Asteroid Adventure Game that need to be drawn on the screen.
 *
 * @version 1.0
 * @since 2023-04-04
 */
public interface Drawable {
    
    /**
     * Draws the current state of the game element on the screen.
     */
    void draw();
}
